# Casting
# Casting is when you change an object's data type to a specific data type.

# From String to Integer
my_var = '10'
print(type(my_var))
my_var_casted=int(my_var)
print('my casted variable as integer: ',type(my_var_casted))

# From Integer/Float to String
my_var = 10
print(type(my_var))
my_var_casted = str(my_var)
print('my casted variable as string: ', type(my_var_casted))


